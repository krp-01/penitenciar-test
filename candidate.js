function el(tag, className, text) {
  const node = document.createElement(tag);
  if (className) node.className = className;
  if (text !== undefined) node.textContent = text;
  return node;
}

function renderSituations() {
  const root = document.getElementById("situations");
  TEST.situations.forEach((item, index) => {
    const box = el("article", "question");
    const top = el("div", "question-top");
    top.append(el("h3", "", `${index + 1}. ${item.title}`), el("span", "points", `${item.points}p`));
    const scenario = el("p", "scenario", `Situație: ${item.scenario}`);
    const q = el("p", "", `Întrebare: ${item.question}`);
    const label = document.createElement("label");
    label.textContent = "Răspunsul tău";
    const textarea = document.createElement("textarea");
    textarea.id = `situation-${index}`;
    textarea.placeholder = "Scrie aici răspunsul complet...";
    label.appendChild(textarea);
    box.append(top, scenario, q, label);
    root.appendChild(box);
  });
}

