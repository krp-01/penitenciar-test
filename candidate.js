function portalUid(prefix="test") {
  if (window.crypto && crypto.randomUUID) return `${prefix}_${crypto.randomUUID()}`;
  return `${prefix}_${Date.now()}_${Math.random().toString(16).slice(2)}`;
}

async function sendToEvaluatorPortal({type, candidateName, code}) {
  if(!window.CloudAPI || !CloudAPI.isConfigured()){
    throw new Error("Backend-ul central nu este configurat. Pune Web App URL în backend-config.js.");
  }
  await CloudAPI.submitTest({
    id: portalUid("test"),
    type,
    candidateName: candidateName || "Candidat Penitenciar",
    code,
    status: "waiting",
    createdAt: new Date().toISOString(),
    source: "candidate-finalize"
  });
}

function collectAnswers() {
  return {
    version: "penitenciar-v2",
    submittedAt: new Date().toISOString(),
    candidateName: document.getElementById("candidateName").value.trim(),
    discordName: document.getElementById("discordName").value.trim(),
    situations: TEST.situations.map((item, index) => ({
      title: item.title,
      points: item.points,
      answer: document.getElementById(`situation-${index}`).value.trim()
    })),
    quiz: TEST.quiz.map((item, index) => {
      const selected = document.querySelector(`input[name="quiz-${index}"]:checked`);
      return {
        question: item.question,
        points: item.points,
        selected: selected ? selected.value : ""
      };
    }),
    rules: TEST.rules.map((item, index) => ({
      title: item.title,
      points: item.points,
      answer: document.getElementById(`rule-${index}`).value.trim()
    }))
  };
}

function buildEvaluatorUrl(payload) {
  const encoded = encodePayload(payload);
  const current = new URL(window.location.href);
  let evaluatorUrl;

  if (current.pathname.includes("/candidat/")) {
    evaluatorUrl = new URL(current.href.replace("/candidat/", "/evaluator/"));
  } else {
    evaluatorUrl = new URL("../evaluator/index.html", current.href);
  }

  evaluatorUrl.hash = `payload=${encoded}`;
  return { url: evaluatorUrl.toString(), code: encoded };
}

async function finalizeTest() {
  const answers = collectAnswers();

  if (!answers.candidateName || !answers.discordName) {
    document.getElementById("statusMessage").textContent = "Completează numele și Discordul înainte să finalizezi testul.";
    return;
  }

  const emptyOpen = answers.situations.filter(x => !x.answer).length + answers.rules.filter(x => !x.answer).length;
  const emptyQuiz = answers.quiz.filter(x => !x.selected).length;
  if (emptyOpen || emptyQuiz) {
    const ok = confirm(`Ai ${emptyOpen + emptyQuiz} răspunsuri necompletate. Vrei să finalizezi totuși testul?`);
    if (!ok) return;
  }

  const { code } = buildEvaluatorUrl(answers);
  try {
    await sendToEvaluatorPortal({
      type: "penitenciar",
      candidateName: answers.candidateName || answers.discordName,
      code
    });
  } catch(err) {
    document.getElementById("statusMessage").textContent = err.message || "Testul nu a putut fi trimis în portalul evaluator.";
    return;
  }

  localStorage.setItem("penitenciarV2Answers", JSON.stringify(answers));
  const shareBox = document.getElementById("shareBox");
  if (shareBox) shareBox.classList.remove("show");
  document.getElementById("statusMessage").textContent = "Test finalizat și trimis automat în portalul evaluator central, la categoria Teste în așteptare.";
}

function generateLink() {
  finalizeTest();
}

async function copyText(text) {
  try {
    await navigator.clipboard.writeText(text);
    document.getElementById("statusMessage").textContent = "Copiat.";
  } catch {
    document.getElementById("statusMessage").textContent = "Nu s-a putut copia automat. Selectează și copiază manual.";
  }
}

function saveLocal() {
  const answers = collectAnswers();
  localStorage.setItem("penitenciarV2Answers", JSON.stringify(answers));
  document.getElementById("statusMessage").textContent = "Răspunsurile au fost salvate local în browser.";
}

function loadLocal() {
  const saved = localStorage.getItem("penitenciarV2Answers");
  if (!saved) return;
  try {
    const answers = JSON.parse(saved);
    document.getElementById("candidateName").value = answers.candidateName || "";
    document.getElementById("discordName").value = answers.discordName || "";
    answers.situations?.forEach((x, i) => {
      const f = document.getElementById(`situation-${i}`);
      if (f) f.value = x.answer || "";
    });
    answers.rules?.forEach((x, i) => {
      const f = document.getElementById(`rule-${i}`);
      if (f) f.value = x.answer || "";
    });
    answers.quiz?.forEach((x, i) => {
      if (x.selected) {
        const input = document.querySelector(`input[name="quiz-${i}"][value="${x.selected}"]`);
        if (input) input.checked = true;
      }
    });
  } catch {}
}

document.addEventListener("DOMContentLoaded", () => {
  renderSituations();
  renderQuiz();
  renderRules();
  loadLocal();

  document.getElementById("generateBtn").addEventListener("click", finalizeTest);
  document.getElementById("saveBtn").addEventListener("click", saveLocal);
  document.getElementById("printBtn").addEventListener("click", () => window.print());
  document.getElementById("copyLinkBtn")?.addEventListener("click", () => copyText(document.getElementById("shareLink").textContent));
  document.getElementById("copyCodeBtn")?.addEventListener("click", () => copyText(document.getElementById("payloadCode").value));
});
